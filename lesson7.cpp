// Write a program to display the following expressions on the screen:
// 2468 + 1234 = 3702
// 2468 - 1234 = 1234
// 2468 * 1234 = 3045512
// 2468 / 1234 = 2
#include <iostream>
using namespace std;
int main(){
    cout << "2468 + 1234 = " << 2468 + 1234;
    cout << endl;
    cout << "2468 - 1234 = " << 2468 - 1234;
    cout << endl;
    cout << "2468 * 1234 = " << 2468 * 1234;
    cout << endl;
    cout << "2468 / 1234 = " << 2468 / 1234;
    cout << endl;
    return 0;
}